public class CycleTitles {
    private String objectId;
    private String cyclePhase;
    private String cyclePhaseTitle;
    private String cyclePhaseStatus;
    private String cyclePhaseExplaination;

    public String getObjectId() {
        return objectId;
    }

    public void setObjectId( String objectId ) {
        this.objectId = objectId;
    }

    public String getCyclePhase() {
        return cyclePhase;
    }

    public void setCyclePhase( String cyclePhase ) {
        this.cyclePhase = cyclePhase;
    }

    public String getCyclePhaseTitle() {
        return cyclePhaseTitle;
    }

    public void setCyclePhaseTitle( String cyclePhaseTitle ) {
        this.cyclePhaseTitle = cyclePhaseTitle;
    }

    public String getCyclePhaseStatus() {
        return cyclePhaseStatus;
    }

    public void setCyclePhaseStatus( String cyclePhaseStatus ) {
        this.cyclePhaseStatus = cyclePhaseStatus;
    }

    public String getCyclePhaseExplaination() {
        return cyclePhaseExplaination;
    }

    public void setCyclePhaseExplaination( String cyclePhaseExplaination ) {
        this.cyclePhaseExplaination = cyclePhaseExplaination;
    }

}
